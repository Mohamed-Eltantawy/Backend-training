<?php

function sum($n1 , $n2){
    return $n1 + $n2 ;
}
function sub($n1 , $n2){
    return $n1 - $n2 ;
}
function mul($n1 , $n2){
    return $n1 * $n2 ; 
}
function div($n1 , $n2){
    return $n1 / $n2 ;
}

echo sum(5,5);
echo '<br>';
echo sub(5,5);
echo '<br>';
echo mul(5,5);
echo '<br>';
echo div(5,5);
echo '<br>';